  private static String processNotesToSwordQuality(String notes) {
    List<Tier> swordTierList = processNotesToTierList(notes);
    return swordTierList.stream().map(t -> String.valueOf(t.center)).collect(Collectors.joining());
  }

  private static List<Tier> processNotesToTierList(String notes) {
    // part1 ignore number before colon
    notes = StringUtils.substringAfter(notes, ":");
    var numList = Arrays.stream(notes.split(",")).map(Integer::parseInt).toList();
    List<Tier> swordTierList = new ArrayList<>();
    for (var num : numList) {
      if (swordTierList.isEmpty()) {
        swordTierList.add(new Tier(0, num, 0));
      } else {
        for (int i = 0; i < swordTierList.size(); i++) {
          var swordTier = swordTierList.get(i);
          if (swordTier.left == 0 && num < swordTier.center) {
            swordTier.left = num;
            break;
          }
          if (swordTier.right == 0 && num > swordTier.center) {
            swordTier.right = num;
            break;
          }
          // If last of the list and num not added, add new element to list
          if (i == swordTierList.size() - 1) {
            swordTierList.add(new Tier(0, num, 0));
            break;
          }
        }
      }
    }
    return swordTierList;
  }

  private static class Tier {
    public int left;
    public int center;
    public int right;

    public Tier(int left, int center, int right) {
      this.left = left;
      this.center = center;
      this.right = right;
    }

    @Override
    public String toString() {
      StringBuilder builder = new StringBuilder();
      builder.append("[").append(left).append(",").append(center).append(",").append(right)
          .append("]");
      return builder.toString();
    }
  }

  private static class Sword implements Comparable<Sword> {
    public int identifier;
    public List<Tier> swordTierList;

    public Sword(int identifier) {
      this.identifier = identifier;
    }

    public void setTier(List<Tier> swordTierList) {
      this.swordTierList = swordTierList;
    }

    @Override
    public int compareTo(Sword other) {
      var thisQuality = Long.parseLong(this.swordTierList.stream()
          .map(t -> String.valueOf(t.center)).collect(Collectors.joining()));
      var otherQuality = Long.parseLong(other.swordTierList.stream()
          .map(t -> String.valueOf(t.center)).collect(Collectors.joining()));
      if (thisQuality != otherQuality) {
        return Long.compare(thisQuality, otherQuality);
      }
      // If quality are same, compare using levels of fishbone
      for (int i = 0; i < swordTierList.size(); i++) {
        var thisQualityLevel = this.swordTierList.get(i);
        var otherQualityLevel = other.swordTierList.get(i);
        // String concatenate
        // Skip concatenate for right if its 0, otherwise will parse integer incorrectly
        // Credits to Melvin
        var thisQualityInt = Integer.parseInt("" + thisQualityLevel.left + thisQualityLevel.center
            + (thisQualityLevel.right > 0 ? thisQualityLevel.right : ""));
        var otherQualityInt =
            Integer.parseInt("" + otherQualityLevel.left + otherQualityLevel.center
                + (otherQualityLevel.right > 0 ? otherQualityLevel.right : ""));
        if (thisQualityInt != otherQualityInt) {
          return Integer.compare(thisQualityInt, otherQualityInt);
        }
      }
      // If all fails, compare using identifier
      return Integer.compare(this.identifier, other.identifier);
    }

    @Override
    public String toString() {
      StringBuilder builder = new StringBuilder();
      builder.append("{identifier:").append(identifier).append(", swordTierList:")
          .append(swordTierList).append("}");
      return builder.toString();
    }
  }
